fun main(args:Array<String>) {
    var i = 10
    var j = 20

    println("Before Swap ......")
    println("I = " +i)
    println("J = " +j)

    var tmp = i
    i = j
    j = tmp

    println("After Swap ......")
    println("I = " +i)
    println("J = " +j)


    println("No: 10 Kotline Prog , By Sanjay D Shirsath")
}